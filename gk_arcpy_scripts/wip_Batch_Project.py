# Import modules
# ---------------------------
import arcpy, sys, string, datetime


# ==============================================================================================================
#                                                                     F U N C T I O N S / D E F I N I T I O N S
# ==============================================================================================================
def msg(msg):
	arcpy.AddMessage(msg)


	
# ==============================================================================================================
#                                                                             I N I T I A L I Z E   S C R I P T
# ==============================================================================================================
	
	

# Script arguments
# --------------------------------------------------------------------------------------------------------------
inShapes = arcpy.GetParameterAsText(0)
outSpRef = arcpy.GetParameterAsText(1)
outFolder = arcpy.GetParameterAsText(2)


# Initialize main variables.
# --------------------------------------------------------------------------------------------------------------
inS = []
for s in inShapes.split(';'):
	inS.append(s)
	msg(s)



# Introduction message (if necessary)
# -------------------------------------------------------------------------------------------------------------	
msg("\n\n")





# ==============================================================================================================
#                                                                                        D O   T H E   W O  R K
# ==============================================================================================================


# Get count total of input layers to be projected
# ---------------------------------------------------------------


msg(len(inS))
msg("\n\n")


for fc in inS:
	d = arcpy.Describe(fc)
	sr = d.spatialReference
	msg(sr.PCSName)




msg("\n\n\n")



	




