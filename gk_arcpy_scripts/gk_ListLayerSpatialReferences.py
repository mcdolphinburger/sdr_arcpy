# Import modules
# ---------------------------
import sys, arcpy, string, datetime
from arcpy import mapping

# ==============================================================================================================
#                                                                     F U N C T I O N S / D E F I N I T I O N S
# ==============================================================================================================
def msg(msg):
	arcpy.AddMessage(msg)


	
# ==============================================================================================================
#                                                                             I N I T I A L I Z E   S C R I P T
# ==============================================================================================================
	
	

# Script arguments
# --------------------------------------------------------------------------------------------------------------
# df = arcpy.GetParameterAsText(0)


# Initialize main variables.
# --------------------------------------------------------------------------------------------------------------
mxd = mapping.MapDocument("CURRENT")
dfs = mapping.ListDataFrames(mxd)


# Introduction message (optional)
# -------------------------------------------------------------------------------------------------------------	
msg("\n\n")



# ==============================================================================================================
#                                                                                        D O   T H E   W O  R K
# ==============================================================================================================

for d in dfs:
	mxd.activeView = d.name
	msg("\nData Frame \"" + d.name + "\"," + d.spatialReference.name)
	lyrs = mapping.ListLayers(mxd, "", d)
	for lyr in lyrs:
		if lyr.isFeatureLayer:
			desc = arcpy.Describe(lyr)
			fc = desc.FeatureClass
			spref = fc.SpatialReference
			msg("  " + lyr.name + "," + spref.name)






msg("\n\nDone!\n\n")



	




